def to_chinese_name_by_code(
    adm2_code: int | None,
    fallback_name: str = "",
) -> str:
    """通过 GAUL ADM2_CODE 获取中文名。

    Args:
        adm2_code: GAUL ADM2_CODE（如 13028）。
        fallback_name: code 未命中时的回退值。

    Returns:
        中文名字符串；未命中则返回 fallback_name（默认 ""）。
    """
    if adm2_code is not None and adm2_code in GAUL_CODE_TO_CHINESE:
        return GAUL_CODE_TO_CHINESE[adm2_code]
    if fallback_name:
        return fallback_name
    return "未知"


def to_chinese_name(pinyin_name: str) -> str:
    """向后兼容：通过英文 / 拼音名查找中文名。

    优先使用 to_chinese_name_by_code（按 ADM2_CODE 查找更准确）。
    本函数仅作为无 code 时的降级路径。

    Args:
        pinyin_name: FAO GAUL ADM2_NAME（如 "Hefei", "Xi'an"）。

    Returns:
        中文名；未命中则返回原字符串。
    """
    if not pinyin_name:
        return pinyin_name

    key = pinyin_name.lower().strip()

    # 精确匹配
    if key in _ENGLISH_TO_CHINESE:
        return _ENGLISH_TO_CHINESE[key]

    # 去除常见标点后重试
    cleaned = key.replace("'", "").replace("-", "").replace(" ", "")
    if cleaned in _ENGLISH_TO_CHINESE:
        return _ENGLISH_TO_CHINESE[cleaned]

    logger.debug(f"Unmapped GAUL district: '{pinyin_name}' (key='{key}')")
    return pinyin_name
